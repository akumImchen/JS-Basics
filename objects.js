// objects in js 
let person ={
    name:" Akum",
    age:23,
    isStudent:true,
};
// we access the object values through dot notation
console.log(person.name);
console.log(person.age);
console.log(person.isStudent);

// we can access it through bracket notation with the key name under ""
console.log(person["name"]);
console.log(person["age"]);

// add a new key value
person.age=22;
console.log(person.age);
delete person.isStudent;
